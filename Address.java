/**
 * This class stores the address information
 */
public class Address
{
    private String street;
    private String city;
    private String state;
    private String zip;

    Address(String street, String city, String state, String zip)
    {
        this.street = street;
        this.city = city;
        this.state = state;
        this.zip = zip;
    }

}
